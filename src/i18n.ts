export const getTranslation = (language: AppLanguage | undefined, key: TranslationKey) => {
  const dictionary = language === 'ar' ? dictionaries.ar : dictionaries.en;
  return dictionary[key] ?? dictionaries.en[key];
};

export const isRtlLanguage = (language: AppLanguage | undefined) => language === 'ar';

export function useI18n() {
  const language = useSettingsStore((state) => state.settings?.language ?? 'en');
  const isRtl = isRtlLanguage(language);
  const t = useCallback((key: TranslationKey) => getTranslation(language, key), [language]);

  return useMemo(() => ({
    language,
    isRtl,
    dir: isRtl ? 'rtl' : 'ltr',
    t,
    days: dayLabels[language],
    shortDays: shortDayLabels[language],
  }), [isRtl, language, t]);
}
